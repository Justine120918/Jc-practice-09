let firstNum = 9; 
let secondNum = 22;

// Addition
let addition = firstNum + secondNum;
console.log(`Addition: ${firstNum} + ${secondNum} = ${addition}`);

// Subtraction
let subtraction = firstNum - secondNum;
console.log(`Subtraction: ${firstNum} - ${secondNum} = ${subtraction}`);

// Multiplication
let multiplication = firstNum * secondNum;
console.log(`Multiplication: ${firstNum} * ${secondNum} = ${multiplication}`);

// Division
let division = firstNum / secondNum;
console.log(`Division: ${firstNum} / ${secondNum} = ${division}`);

// Modulus
let modulus = firstNum % secondNum;
console.log(`Modulus: ${firstNum} % ${secondNum} = ${modulus}`);

// Exponentiation (Power)
let exponentiation = firstNum ** secondNum;
console.log(`Exponentiation: ${firstNum} ** ${secondNum} = ${exponentiation}`);
